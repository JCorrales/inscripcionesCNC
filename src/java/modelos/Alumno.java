package modelos;

/**
 *
 * @author Acer Aspire
 */
public class Alumno {
    private int cedula;
    private String nombre;
    private String apellido;
    private String direccion;
    private String telefono;
    private int anio;

    public Alumno() {
    }

    public Alumno(int cedula, String nombre, String apellido, String direccion, String telefono, int anio) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.apellido = apellido;
        this.direccion = direccion;
        this.telefono = telefono;
        this.anio = anio;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    @Override
    public String toString() {
        return ""+cedula+"\n"+nombre+"\n"+apellido+"\n"+direccion+"\n"+telefono+"\n"+anio; //To change body of generated methods, choose Tools | Templates.
    }
    
    
}
